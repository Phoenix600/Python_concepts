# define all basic operation function here 

def add(a,b):
    """@add() to add two number """
    return a+b

def subtract(a,b) :
    """ @subtract() to subtract two number"""
    return a - b

def multiply(a,b): 
    """ @multiply() to get product of two numbers """
    return a * b

def division(a,b) :
    return a/b


